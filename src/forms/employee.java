    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
String search = "select * from employee where ";
if(btnrNumber.isSelected()){
search += "EmpNo";
}else if(btnrName.isSelected()){
search += "EmpName";
} else if(btnrAddres.isSelected()){
search += "EmpAddres";
}else if(btnrSalary.isSelected()){
search += "EmpSalary";
}else if(btnrHiring.isSelected()){
search += "HiringDate";
}else if(btnrBirth.isSelected()){
search += "BirthDate";
}else if(btnrDepartement.isSelected()){
search += "DeptNo";
}
search += " like ('%"+ txtSerch.getText() + "%')";
emp.getCustemRow(search, tblEmp);

    }//GEN-LAST:event_btnSearchActionPerformed

