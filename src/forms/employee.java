    public void SetValues(){
emp.setEmpNo(Integer.parseInt(txtNumber.getText()));
emp.setEmpName(txtName.getText());
emp.setEmpAddres(txtAddres.getText());
emp.setEmpSalary(Double.parseDouble(txtSalary.getText()));
    SimpleDateFormat f  = new SimpleDateFormat("yyyy-MM-dd");
    String HiringD = String.valueOf(f.format(dHiring.getDate()));
    String Birthd = String.valueOf(f.format(dBirth.getDate()));
    emp.setHiringDate(HiringD);
    emp.setBirthDate(Birthd);
    String dName = cDept.getSelectedItem().toString();
    String dNo = dept.getValuebyName(dName);
    emp.setDeptNo(Integer.parseInt(dNo));


}
