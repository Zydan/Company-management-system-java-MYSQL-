    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
try{
        String search = "select * from workon_data where ";
if(btnrEmpNumber.isSelected()){
search += "Employee_no";

}else if(btnrEmpName.isSelected()){
search += "Employee_Name";
}else if(btnrProjNumber.isSelected()){
search += "Project_No" ;
}else if(btnrProjName.isSelected()){
search += "Project_Name";
}
search += " like ('%"+ txtSearch.getText() + "%')";

work.getCustemRow(search, tblWork);
}catch(Exception ex){Tools.mBox(ex.getMessage());}
    }//GEN-LAST:event_btnSearchActionPerformed

