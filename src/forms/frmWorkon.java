    Employee emp = new Employee();
Project pro = new Project();
WorkOn work = new WorkOn();
private void setValues(){
    try{
String EmpName = CEmp.getSelectedItem().toString();
String ProjName = Cproj.getSelectedItem().toString();

String strEmpNo = emp.getValuebyName(EmpName);
String ProjNo = pro.getValuebyName(ProjName);

int EmpNo = Integer.parseInt(strEmpNo);
int PrjectNo = Integer.parseInt(ProjNo);
work.setEmpNo(EmpNo);
work.setProjectNo(PrjectNo);
    }catch(Exception ex){Tools.mBox(ex.getMessage());}

}

